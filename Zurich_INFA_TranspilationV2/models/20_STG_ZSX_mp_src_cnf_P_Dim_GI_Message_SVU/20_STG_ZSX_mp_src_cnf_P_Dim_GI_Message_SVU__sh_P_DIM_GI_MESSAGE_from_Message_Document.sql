    CTL_SRC_ROW_ID AS CTL_SRC_ROW_ID,
    CTL_BATCH_ID AS CTL_BATCH_ID,
    CTL_JOB_ID AS CTL_JOB_ID,
    CTL_REC_CRTN_DATE AS CTL_REC_CRTN_DATE,
    CTL_EXTRACT_ID AS CTL_EXTRACT_ID,
    CTL_SRC_SYS_SET_NAME AS CTL_SRC_SYS_SET_NAME,
    MESSAGE_TYPE AS MESSAGE_TYPE,
    MESSAGE_PACKET AS MESSAGE_PACKET,
    MESSAGE_TS AS MESSAGE_TS,
    MESSAGE_STATUS_CODE AS MESSAGE_STATUS_CODE,
    MESSAGE_TYPE_CODE AS MESSAGE_TYPE_CODE,
    MESSAGE_DIRECTION AS MESSAGE_DIRECTION,
    MESSAGE_PACKET_DOC_TYPE AS MESSAGE_PACKET_DOC_TYPE,
